package setinterface;

import java.util.HashSet;
import java.util.Set;

public class SetExample {
	
	
	public static void main(String args[]) {
		Set<Object> hashset = new HashSet<Object>();
		hashset.add("rose");
		hashset.add("green");
		hashset.add("yellow");
		System.out.println(hashset);
	}

}
